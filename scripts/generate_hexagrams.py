def calc_fuxi_id(bits):
    """bits = [上3爻, 下3爻] or 6爻从下往上"""
    # bits are 6 values, index 0=bottom, 5=top
    lower = bits[0] + (bits[1] << 1) + (bits[2] << 2)
    upper = bits[3] + (bits[4] << 1) + (bits[5] << 2)
    return upper * 8 + lower

def get_trigram_name(idx):
    return TRIGRAMS[idx]

# Build Fu Xi ordered array
hexagrams_by_fuxi = [None] * 64

for kw_id, name, symbol, bits, description in KING_WEN:
    fid = calc_fuxi_id(bits)
    lower_bin = bits[0] + (bits[1] << 1) + (bits[2] << 2)
    upper_bin = bits[3] + (bits[4] << 1) + (bits[5] << 2)
    hexagrams_by_fuxi[fid] = {
        'id': fid,
        'name': name,
        'symbol': symbol,
        'upperTrigram': get_trigram_name(upper_bin),
        'lowerTrigram': get_trigram_name(lower_bin),
        'description': description,
        'vernacular': '',
        'interpretations': {
            'love': '',
            'work': ''
        }
    }

